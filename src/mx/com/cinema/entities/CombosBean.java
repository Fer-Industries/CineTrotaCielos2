package mx.com.cinema.entities;

public class CombosBean {

	private int comboId;
	private String comboNombre;
	private float comboPrecio;
	private String comboStatus;
	
	 public CombosBean() {
			
		}
	
	public int getComboId() {
		return comboId;
	}
	public void setComboID(int comboID) {
		this.comboId = comboID;
	}
	public String getComboNombre() {
		return comboNombre;
	}
	public void setComboNombre(String comboNombre) {
		this.comboNombre = comboNombre;
	}
	public float getComboPrecio() {
		return comboPrecio;
	}
	public void setComboPrecio(float comboPrecio) {
		this.comboPrecio = comboPrecio;
	}
	public String getComboStatus() {
		return comboStatus;
	}
	public void setComboStatus(String comboStatus) {
		this.comboStatus = comboStatus;
	}
	
	
}
