package mx.com.cinema.entities;

public class FormatosBean {
	
	private int idFormato;
	private String nombreFormato;
	private int precioFormato;
	
	public FormatosBean() {
		
	}

	public int getIdFormato() {
		return idFormato;
	}

	public void setIdFormato(int idFormato) {
		this.idFormato = idFormato;
	}

	public String getNombreFormato() {
		return nombreFormato;
	}

	public void setNombreFormato(String nombreFormato) {
		this.nombreFormato = nombreFormato;
	}

	public int getPrecioFormato() {
		return precioFormato;
	}

	public void setPrecioFormato(int precioFormato) {
		this.precioFormato = precioFormato;
	}
	
   
}
